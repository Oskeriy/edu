# Atomic Data types (or basic data types)

# int, float, bool

a = 1  # numbers without floating point are called int
b = 1.5  # numbers with a floating point are called float

# boolean values can be either True or False
c = True
d = False

# You can change the values of the variables

print(a)  # outputs 1
a = a + 2
print(a)  # outputs 3

# You can change the values of the boolean values via 'not' like so:
print(c)  # here it is True
c = not c
print(c)  # not it is False

# A simple collection data type is
# String is a set of characters, or a word
this_string = "Hello"
print(this_string)

# These are the operators: +, -, *, /
# This is remainder operator
print(5 % 2)  # will output 1, since it is a remainder

# ** this operator is for powers
print(2 ** 3)  # will output 9, or 2 to the power of 3
